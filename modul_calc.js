const add = (num1, num2) => {
  return num1 + num2;
};

const subtr = (num1, num2) => {
  return num1 - num2;
};

const mult = (num1, num2) => {
  return num1 * num2;
};

const div = (num1, num2) => {
  return num1 / num2;
};

module.exports = {
  add: add,
  subtr: subtr,
  mult: mult,
  div: div
};
